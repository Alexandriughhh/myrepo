library(tidyverse)
library(caret)
library(MASS)

# Create dataset
set.seed(1)
n <- 100
Sigma <- 9 * matrix(c(1.0, 0.5, 0.5, 1.0), 2, 2)
dat <- MASS::mvrnorm(n = 100, c(69, 69), Sigma) %>%
  data.frame() %>% setNames(c("x", "y"))

# Run 100 simulations
set.seed(1)
rmse_vals <- replicate(100, {
  # Split the data
  test_index <- createDataPartition(dat$y, p = 0.5, list = FALSE)
  train_set <- dat[test_index, ]
  test_set <- dat[-test_index, ]
  
  # Train the model
  fit <- lm(y ~ x, data = train_set)
  
  # Predict on test set
  y_hat <- predict(fit, newdata = test_set)
  
  # Calculate RMSE
  sqrt(mean((y_hat - test_set$y)^2))
})

# Report mean and SD of RMSEs
mean_rmse <- mean(rmse_vals)
sd_rmse <- sd(rmse_vals)

cat("Mean RMSE:", round(mean_rmse, 3), "\n")
cat("SD of RMSE:", round(sd_rmse, 3), "\n")

